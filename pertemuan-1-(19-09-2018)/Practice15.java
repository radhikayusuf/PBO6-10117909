class Practice15 {

    public static void main(String[] args){

        boolean valueOfA = true;
        boolean valueOfB = false;

        System.out.printf("a && b = %b\r\n", (valueOfA && valueOfB));
        System.out.printf("a || b = %b\r\n", (valueOfA || valueOfB));
        System.out.printf("!a = %b\r\n", (!valueOfA));
        System.out.printf("!(a && b)) = %b\r\n", !(valueOfA && valueOfB));
    }

}
    